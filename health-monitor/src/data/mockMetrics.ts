const DEFAULT_JITTER: [number, number, number] = [6, 0, 100];

export function createInitialMetrics(): MetricValues {
  return { ...B };
}

export function tickMetrics(current: MetricValues): MetricValues {
  const next = { ...current };
  for (const key of Object.keys(next)) {
    const [delta, min, max] = JITTER[key] ?? DEFAULT_JITTER;
    if (delta === 0) continue; // truly static
    const change = (Math.random() - 0.5) * delta;
    let v = current[key] + change;
    // Binary rounding
    if (max <= 1) v = Math.round(Math.min(max, Math.max(min, v)));
    else v = Math.round(Math.min(max, Math.max(min, v)) * 10) / 10;
    next[key] = v;
  }
  return next;
}
